package com.scpakar.scpakarweb.dto;

import java.io.Serializable;

/**
 *
 * @author nicolas.canaan
 */
public enum Mensaje implements Serializable{
    
    INVALIDO("INVÁLIDO"),
    ACCESO("ACCESO INCORRECTO"),
    ERROR("ERROR EN OPERACIÓN, INTENTE NUEVAMENTE"),
    NO_IGUAL("VALIDAR");
    
    private String mensaje;

    private Mensaje(String mensaje) {
        this.mensaje = mensaje;
    }

    public String getMensaje() {
        return mensaje;
    }
    
}
