package com.grupopakar.grupopakarappws.dto;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

/**
 *
 * @author alberto.quirino
 */
public class ActividadDTO implements Serializable {

    private int idGrupoTarea;
    private String grupoTarea;
    private List<TareaDTO> listaTarea;

    public ActividadDTO() {
        this.idGrupoTarea = 0;
        this.grupoTarea = "";
        this.listaTarea = new ArrayList<>();
    }

    public int getIdGrupoTarea() {
        return idGrupoTarea;
    }

    public void setIdGrupoTarea(int idGrupoTarea) {
        this.idGrupoTarea = idGrupoTarea;
    }

    public String getGrupoTarea() {
        return grupoTarea;
    }

    public void setGrupoTarea(String grupoTarea) {
        this.grupoTarea = grupoTarea;
    }

    public List<TareaDTO> getListaTarea() {
        return listaTarea;
    }

    public void setListaTarea(List<TareaDTO> listaTarea) {
        this.listaTarea = listaTarea;
    }
        
}
