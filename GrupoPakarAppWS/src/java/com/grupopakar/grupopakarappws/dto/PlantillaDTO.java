package com.grupopakar.grupopakarappws.dto;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

/**
 *
 * @author pablo.martinez
 */
public class PlantillaDTO implements Serializable {

    private int vacantes;
    private int plantillaAutorizada;
    private List<EmpleadoDTO> plantilla;

    public PlantillaDTO() {
        this.plantilla = new ArrayList<>();
    }

    public int getVacantes() {
        return vacantes;
    }

    public void setVacantes(int vacantes) {
        this.vacantes = vacantes;
    }

    public int getPlantillaAutorizada() {
        return plantillaAutorizada;
    }

    public void setPlantillaAutorizada(int plantillaAutorizada) {
        this.plantillaAutorizada = plantillaAutorizada;
    }

    public List<EmpleadoDTO> getPlantilla() {
        return plantilla;
    }

    public void setPlantilla(List<EmpleadoDTO> plantilla) {
        this.plantilla = plantilla;
    }

    @Override
    public String toString() {
        return "PlantillaDTO{" + "vacantes=" + vacantes + ", plantillaAutorizada=" + plantillaAutorizada + ", plantilla=" + plantilla + '}';
    }

}
