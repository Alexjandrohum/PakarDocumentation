package com.grupopakar.grupopakarappws.dto;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

/**
 *
 * @author carlos.juarez
 */
public class UsuarioDTO implements Serializable {

    private String tokenDeUsuario;
    private String nombre;
    private String numeroControl;
    private int notificacionesPendientes;
    private List<ModuloDTO> listaModulos;

    public UsuarioDTO() {
        listaModulos = new ArrayList<ModuloDTO>();
    }

    public String getTokenDeUsuario() {
        return tokenDeUsuario;
    }

    public void setTokenDeUsuario(String tokenDeUsuario) {
        this.tokenDeUsuario = tokenDeUsuario;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNumeroControl() {
        return numeroControl;
    }

    public void setNumeroControl(String numeroControl) {
        this.numeroControl = numeroControl;
    }

    public int getNotificacionesPendientes() {
        return notificacionesPendientes;
    }

    public void setNotificacionesPendientes(int notificacionesPendientes) {
        this.notificacionesPendientes = notificacionesPendientes;
    }

    public List<ModuloDTO> getListaModulos() {
        return listaModulos;
    }

    public void setListaModulos(List<ModuloDTO> listaModulos) {
        this.listaModulos = listaModulos;
    }
}
