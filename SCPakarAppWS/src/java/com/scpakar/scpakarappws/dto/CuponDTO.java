package com.scpakar.scpakarappws.dto;

import java.io.Serializable;

/**
 *
 * @author antonio.ruiz
 */
public class CuponDTO implements Serializable {

    private String idCupon;
    private String nombre;
    private String vigencia;
    private int entregados;
    private int aplicados;
    private int disponibles;
    private String imagen;
    private EstatusCupon estatus;

    public CuponDTO() {
        this.idCupon = "";
        this.nombre = "";
        this.vigencia = "";
        this.entregados = 0;
        this.aplicados = 0;
        this.disponibles = 0;
        this.imagen = "";
        this.estatus = EstatusCupon.HISTORIAL;
    }

    public String getIdCupon() {
        return idCupon;
    }

    public void setIdCupon(String idCupon) {
        this.idCupon = idCupon;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getVigencia() {
        return vigencia;
    }

    public void setVigencia(String vigencia) {
        this.vigencia = vigencia;
    }

    public int getEntregados() {
        return entregados;
    }

    public void setEntregados(int entregados) {
        this.entregados = entregados;
    }

    public int getAplicados() {
        return aplicados;
    }

    public void setAplicados(int aplicados) {
        this.aplicados = aplicados;
    }

    public int getDisponibles() {
        return disponibles;
    }

    public void setDisponibles(int disponibles) {
        this.disponibles = disponibles;
    }

    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }

    public EstatusCupon getEstatus() {
        return estatus;
    }

    public void setEstatus(EstatusCupon estatus) {
        this.estatus = estatus;
    }
}
