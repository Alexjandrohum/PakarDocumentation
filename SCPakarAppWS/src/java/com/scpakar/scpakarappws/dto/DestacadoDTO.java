package com.scpakar.scpakarappws.dto;

/**
 *
 * @author antonio.ruiz
 */
import java.io.Serializable;

public class DestacadoDTO implements Serializable {
    private int idDestacado;
    private String urlImagen;
    private ProductoDTO producto;
    private int idLista;
    private int idModulo;

    public DestacadoDTO() {
        this.idDestacado = 0;
        this.urlImagen = "";
        this.idLista = 0;
        this.idModulo = 0;
    }

    public int getIdDestacado() {
        return idDestacado;
    }

    public void setIdDestacado(int idDestacado) {
        this.idDestacado = idDestacado;
    }

    public String getUrlImagen() {
        return urlImagen;
    }

    public void setUrlImagen(String urlImagen) {
        this.urlImagen = urlImagen;
    }

    public ProductoDTO getProducto() {
        return producto;
    }

    public void setProducto(ProductoDTO producto) {
        this.producto = producto;
    }

    public int getIdLista() {
        return idLista;
    }

    public void setIdLista(int idLista) {
        this.idLista = idLista;
    }

    public int getIdModulo() {
        return idModulo;
    }

    public void setIdModulo(int idModulo) {
        this.idModulo = idModulo;
    }
}
