package com.scpakar.scpakarappws.dto;

import java.io.Serializable;
import java.util.List;

/**
 * Created by antonio.ruiz on 2019-06-21
 */
public class CabeceraCuponDTO implements Serializable {
    private int numero;
    private EstatusCupon estatusCupon;
    private List<CuponDTO> cupones;

    public CabeceraCuponDTO() {
    }

    public CabeceraCuponDTO(int numero, EstatusCupon estatusCupon, List<CuponDTO> cupones) {
        this.numero = numero;
        this.estatusCupon = estatusCupon;
        this.cupones = cupones;
    }

    public List<CuponDTO> getCupones() {
        return cupones;
    }

    public void setCupones(List<CuponDTO> cupones) {
        this.cupones = cupones;
    }

    public EstatusCupon getEstatusCupon() {
        return estatusCupon;
    }

    public void setEstatusCupon(EstatusCupon estatusCupon) {
        this.estatusCupon = estatusCupon;
    }
    
    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }
}
